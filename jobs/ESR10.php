	<h2>ESR 10 - Technology Assessment and Exploitation</h2>
	<p>This position is with EDUSAFE partner The Athens University of
		Economics & Business (AUEB).</p>
	<p>Early Stage Researcher (ESR) position for Technology assessment and
		Exploitation</p>
	<p>As an ESR, you will perform a comprehensive technology assessment of
		the CERN/ATLAS Augmented Reality system for operation in hazardous areas
		and use it as a framework for assessments of other extreme environments, 
		taking into account specific requirements of that environment and possible 
		end-user specifications. You will map the technical specifications
		and requirements in great detail in order to be able to have a system
		which can be adjusted and modified for other markets. It will be
		turned into a methodology which allows taking other uses and
		requirements into account whilst developing the primary prototype, so
		that future development costs and time-to-market can be reduced. You
		will investigate all intellectual property and licensing ramifications
		with a view to supporting a detailed exploitation plan for use of the
		technology in other sectors.</p>
	<p>This task is part of a larger international collaboration coordinated 
		by the ATLAS experiment at the Large Hadron Collider, LHC, at CERN, 
		aiming to develop a novel Augmented Reality system for operation in 
		hazardous environments. As such, you will also be part of the global system
		prototype creation and testing campaigns in the LHC ATLAS experiment 
		enviroment.</p>
	<p>Preferred university diploma: MSc/Diploma degree with a strong
		background in Computing, Informatics and Information Systems.</p>
	<p>Requirements:</p>
	<ul>
		<li>Analytical and computing skills and working experience both as a
			developer/ software engineer and as a systems analyst.</li>
		<li>Strong interest in a substantial research project, ability to work
			independently, persistence in the face of setbacks and
			self-discipline are important qualities that will be taken into
			consideration.</li>
		<li>Good knowledge of English; basic knowledge of Greek or an
			undertaking to acquire it rapidly.</li>

	</ul>
	<p>You will spend up to 30% of your contract on secondment with EDUSAFE partners
		including at Novocaptis, in Greece, at PRISMA, in Greece, at CERN
		in Switzerland and at Canberra in France.</p>
	<p>You will participate in network wide activities with ESRs from the
		other EDUSAFE partners, workshops, summer schools and conferences.</p>
	<p>You will be offered the possibility to study for a PhD in the Department
		of Informatics of the Athens University of Economics and Business (AUEB),
		using the research work performed within EDUSAFE project to obtain a PhD 
		at the end of the 3-year contract.</p>
	<p>The ESR will be under employment contract with the Athens University of 
		of Economics and Business (AUEB). You will be based in Athens, Greece. 
		The duration of the contract will be 3 years expected to start from about 
		January 2013, full time.</p>

