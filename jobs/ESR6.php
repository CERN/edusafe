	<h2>ESR 6 - System Modularity and Integration Optimization</h2>

	<p>This position is with EDUSAFE partner PRISMA Electronics SA, Greece</p>
	<p>Early Stage Researcher (ESR) assignement is the 'System Modularity and 
		Integration Optimization development'</p>
	<p>As an ESR, the aim of your work is the design and integration 
		optimization of an embedded system for supporting and
		enhancing the security of engineers/technicians working in hazardous
		environments. You will study the development and implementation of a
		fully integrated system comprising the results extracted from the
		research efforts of the various groups of the project. Essential part of this 
		will be the optimization of the interfaces among electronics
		devices to attain compatibility, modularity and scalability
		of the system. In addition, you will also study and adapt software protocols
		compatibility and perform a modularity study. The goal is to study
		system adaptability with regard to new emerging technologies and new
		markets concerning software protocols, electronics design and
		subsystems interfaces.</p>
	<p>This task is part of a larger international collaboration coordinated 
		by the ATLAS experiment at the Large Hadron Collider, LHC, at CERN, 
		aiming to develop a novel Augmented Reality system for operation in 
		hazardous environments. As such, you will also be part of the global system
		prototype creation and testing campaigns in the LHC ATLAS experiment 
		enviroment.</p>
	<p>Preferred university diploma: MSc/Diploma degree in
		Electrical/Electronic Engineering & ICT</p>
	<p>Academinc requirements:</p>
	<ul>
		<li>Good knowledge on Embedded Systems Engineering is required</li>
		<li>Wireless network experience/knowledge will be appreciated</li>
		<li>Strong interest in a substantial research project, ability to work
			independently, persistence in the face of setbacks and
			self-discipline are important qualities taken into consideration</li>
		<li>Good knowledge of English; basic knowledge of Greek or an
			undertaking to acquire it rapidly</li>
	</ul>
	<p>You will spend up to 30% of your contract on secondments with
		EDUSAFE partners including University of Roma "Tor Vergata", in Italy, 
		for electronics miniaturization, chip development from conceptual design,
		to prototyping and large scale production, and at CERN, in Switzerland,
		for environment and usability requirements for the LHC use case, large
		infrastructure wireless architecture design, DAQ architecture and 
		development for large scale scientific infrastructures.</p>
	<p>You will participate in network wide activities with ESRs from the
		other EDUSAFE partners, workshops, summer schools and conferences.</p>
	<p>As an ESR, you will be under employment contract with Prisma
		Electronics SA. You will be based in Alexandroupolis, Greece.
		The duration of the contract is 3 years expected to start from about
		February 2013, full time.</p>
	<p>Finally, you will be offered the possibility to study for a PhD in the
		Department of Electrical & Computer Engineering at the Democritus
		University of Thrace (DUTH) in Xanthi (80km close to Alexandroupolis),
		using the research work performed within the EDUSAFE project, to obtain
		a PhD at the end of the 3-year contract.</p>
