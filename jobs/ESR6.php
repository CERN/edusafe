	<h2>ESR 6 - PSS System Modularity/Integration Optimization</h2>

	<p>This position is with EDUSAFE partner Prisma Electronics SA,
		Alexandroupolis, Greece</p>
	<p>Early Stage Researcher (ESR) position for PSS system
		modularity/integration optimization</p>
	<p>The focus of this research and development activity is the design
		and integration optimization of an embedded system for supporting and
		enhancing the security of technicians working in hazardous
		environments. You will study the development and implementation of a
		fully integrated system comprising the results extracted from the
		research efforts of the various groups on EUDSAFE. An essential part
		will be the optimization of the interfaces among electronics
		components/devices to attain compatibility, modularity and scalability
		of the system. You will also study and adapt software protocols
		compatibility and perform a modularity study. The goal is to study
		system adaptability with regard to new emergency technologies and new
		markets concerning software protocols, electronics design and
		subsystems interfaces.</p>
	<p>You will spend two months of your contract on secondment with
		EDUSAFE partners : 1 month at Universit&agrave; degli Studi di Roma
		"Tor Vergata" and 1 month at CERN.</p>
	<p>Preferred university diploma: MSc/Diploma degree in
		Electrical/Electronic Engineering & ICT</p>
	<p>Requirements:</p>
	<ul>
		<li>Good knowledge on Embedded Systems Engineering is required</li>
		<li>Wireless network experience/knowledge will be appreciated</li>
		<li>Strong interest in a substantial research project, ability to work
			independently, persistence in the face of setbacks and
			self-discipline are important qualities taken into consideration</li>
		<li>Good knowledge of English; basic knowledge of Greek or an
			undertaking to acquire it rapidly</li>
	</ul>
	<p>You will participate in network wide activities with ESRs from the
		other EDUSAFE partners, workshops, summer schools and conferences.</p>
	<p>You will be offered the possibility to study for a PhD in the
		Department of Electrical & Computer Engineering at the Democritus
		University of Thrace (DUTH). The research performed within EDUSAFE
		will be valid to obtain the PhD at the end of the 3-year appointment.
	</p>
	<p>This ESR position will be under employment contract with Prisma
		Electronics SA. The successful candidate will be based in
		Alexandroupolis, Greece; DUTH is in Xanthi (85 km away). The duration
		of the contract is 3 years starting February 2013, full time.</p>
