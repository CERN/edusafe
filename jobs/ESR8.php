	<h2>ESR 8 - Next generation gamma imager development</h2>
	<p>This position is with EDUSAFE partner Canberra in Montigny, 
		France.</p>
	<p>Early Stage Researcher (ESR) position on next generation gamma
		imager developments</p>
	<p>As an ESR, You will provide scientific, programming and application support to
		the new Canberra Gamma Imager. You will strengthen the level of scientific
		excellence within the R&D Team trough strong interactions with the
		Chief Architects, Product Managers, external researchers (CERN) and
		customers.</p>
	<p>You will support development of  analysis algorithms to couple visible
		and gamma images. You will also work on dose rate calculations which is
		a function of the distances between the source and the camera. Radiation 
		energy will be studied; as a preliminary study, spectroscopic capabilities
		will be studied in order to discriminate between and identify radioelements.</p>
	<p>This task is part of a larger international collaboration headed by 
		the ATLAS experiment at the Large Hadron Collider, LHC, at CERN,
		aiming to develop a novel Augmented Reality system for operation in
		hazardous areas. As such, you will also be part of a global system 
		protoype creation and testing campaigns in the LHC ATLAS experiment 
		environment.</p>
	<p>The studies will be made in close partnership with the LPC
		(Laboratoire de Physique Corpusculaire), Caen, which will be the
		academic link in France, and CERN.</p>
	<p>Preferred university diploma: MSc/Diploma degree in software
		engineering</p>
	<p>Requirements:</p>
	<ul>
		<li>High level of hands on experience with nuclear instrumentation,
			programming languages and modeling tools mandatory: Windows / Linux
			embedded environment, C++ and JAVA programming.</li>
		<li>Interest in collaborating on projects with customers and potential
			external partners</li>
		<li>Interest in learning new technologies and creativity in applying
			them to customer applications</li>
		<li>Interest in the product development and management process</li>
		<li>Excellent written and oral presentation skills</li>
		<li>Good knowledge of English; basic knowledge of French or an
			undertaking to acquire it rapidly</li>
	</ul>
	<p>You will spend up to 30% of your contract on secondments with EDUSAFE 
		partners at Ecole Polytechnique F&eacute;d&eacute;rale de Lausanne, EPFL, 
		in Switzerland, for computer vision, camera registration, requirements for
		AR applications and at CERN in Switzerland, for Hybrid silicon pixel detectors
		technology for tracking applications, high-enery physics experiments and 
		accelerators irradiation and activation profiles, complex scientific infrastructure 
		radiation protection strategy, LHC use case requirements.</p>
	<p>You will participate in network wide activities with ESRs from the
		other EDUSAFE partners, workshops, summer schools and conferences.</p>
	<p>You will be offered the possibility to study for a PhD at the
		University of Caen, France. The research performed within EDUSAFE will
		contribute towards the PhD at the end of the 3-year appointment.</p>
	<p>This ESR position will be under employment contract with Canberra.
		You will be based in Montigny, France. The duration of the contract is 3
		years expection to start from January 2013, full time.</p>

