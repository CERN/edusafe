	<h2>ESR 9 - Augmented-Reality-based display and interaction environment</h2>
	<p>This position is with Technische Universit&auml;t M&uuml;nchen (TUM), Munich,
		Germany</p>
	<p>Early Stage Researcher (ESR) position for Augmented-Reality-based
		display and interaction environment</p>
	<p>As an ESR in computer science and Augmented Reality (AR), you will
		perform research towards developing AR-based display and interaction
		methodologies using, for example, HMDs, tablet PCs and/or stationary
		3D monitors with novel input facilities (multi-touch, motion) to
		support maintenance workers in extreme environments. The system will
		support workers in safety-critical tasks, providing them with
		information in ergonomically and psychophysically acceptable ways from
		computer systems and remote experts - without unduly overstraining or
		distracting them.</p>
	<p>You will participate in eliciting requirements for information
		presentation setups with CERN for 2-3 realistic application scenarios
		and will study a design space of options towards using one (or a
		collection of combined) display(s). You will select a flexible
		architecture to use display subsets for specific user-centered
		evaluations in specific scenarios with focus on specific cognitive,
		physical or technical issues.</p>
	<p>You will spend two months on secondment with EDUSAFE partners (one
		month at EPFL, Switzerland on computer Vision, patch based recognition
		techniques, 3D reconstruction and pose determination) and one month at
		CERN, Switzerland on LHC use case requirements for the visualizer
		operability and information visualization in case of operational and
		emergency</p>
	<p>Preferred university diploma: MSc/Diploma degree in Computer Science
		or Computer Engineering</p>
	<p>You should have a good knowledge of English; basic knowledge of
		German or an undertaking to acquire it rapidly.</p>
	<p>You will participate in network wide activities with ESRs from the
		other EDUSAFE partners, workshops, summer schools and conferences.</p>
	<p>You will be offered the possibility to study for a PhD at TUM. The
		research performed within EDUSAFE will be valid to obtain the PhD at
		the end of the 3-year appointment.</p>
	<p>This ESR position will be under employment contract with the
		Fakult&auml;t f&uuml;r Informatik of the TUM. You will be based in Munich,
		Germany. The duration of the contract is 3 years starting in January
		2013, full time.</p>

