	<h2>3D tracking system development</h2>

	<h3>Fellowship Description</h3>
	<p>The candidate will develop the 3D tracking system used for the
		Augmented Reality applications within EDUSAFE, and help supervising
		the other EPFL candidate during his PhD. The developed system will
		exploit visual information together with the measures captured from an
		accelerometer and a gyroscope. This will be done in close
		collaboration with NOCA, who will develop the hardware platform, the
		PhD candidate at EPFL and ROMA who will develop the core Computer
		Vision techniques, and CERN, who will develop the virtual content
		visualizer.</p>

	<h3>Academic Requirements</h3>
	<p>The candidate must hold a PhD's degree in Computer Vision.</p>

	<h3>Location</h3>
	<p>This fellowship will be under employment contract with EPFL. The
		candidate will be based in Lausanne, Switzerland.</p>

	<h3>Secondments</h3>
	<p>Roma2, NOCA, TUM, CAN</p>

	<h3>Duration of fellowship</h3>
	<p>Starting from about January 2014, for a period of 2 years. This post
		is full time and fixed-term.</p>

