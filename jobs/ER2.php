	<h2>ER 2 - 3D tracking system development</h2>

	<p>This position is with EDUSAFE partner Ecole Polytechnique F&eacute;d&eacute;rale
		de Lausanne (EPFL), Switzerland</p>
	<p>Experienced Researcher (ER) position for 3D tracking system
		development</p>
	<p>As an ER, you will develop the 3D tracking system used for the
		Augmented Reality applications within EDUSAFE, and help supervise an
		Early Stage Researcher who will be doing a PhD at EPFL. The developed
		system will exploit visual information together with the measures
		captured from an accelerometer and a gyroscope. This will be done in
		close collaboration with EDUSAFE partner Novocaptis, Greece, who will
		develop the hardware platform, the PhD candidate at EPFL and the
		University of Rome "Tor Vergata" who will develop the core Computer
		Vision techniques, and CERN who will develop the virtual content
		visualizer.</p>
	<p>This task is part of a larger international collaboration coordinated by 
		the ATLAS experiment at the Large Hadron Collider, LHC, at CERN aiming 
		to develop a novel Augmented Reality system for operation in hazardous 
		areas. As such, you will also be part of the global system prototype 
		creation and testing campaigns in the LHC ATLAS experiment environment.</p>
	<p>Requirements:</p>
	<ul>
		<li>PhD in Computer Vision</li>
		<li>Outstanding track record of publications in top Computer Vision or 
			Robotics conferences and journals</li>
		<li>Good knowledge of English; basic knowledge of French or an
			undertaking to acquire it rapidly</li>
	</ul>
	<p>You will spend up to 30% of your contract on secondments with
		EDUSAFE partners, including the University of Roma2 "Tor Vergata" in Italy,
		for WRM methodology development, associated chip design, parametric space
		based algorithm development techniques and at Novocaptis in Greece, and at 
		the Aristotle University of Thessaloniki, AUTH, in Greece, for video and 
		sensing unit development and data transfer for 3D pose, system architecture 
		design, and training on business and new product development process
		and at CERN, in Switzerland, for project management, system integration 
		and validation process methodology, AR system environment and usability 
		requirements for the LHC use case.</p>
	<p>You will participate in network wide activities such as common
		activities with researchers from the other EDUSAFE partners,
		workshops, summer schools and conferences.</p>
	<p>This ER position will be under employment contract with EPFL, Ecole 
		Polytechnique F&eacute;d&eacute;rale de Lausanne. You will be based 
		in Lausanne, Switzerland. The duration of the contract
		is 2 years expecting to start from about January 2014, full time.</p>
	
	
