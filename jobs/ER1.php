	<h2>ER 1 - WRM Chip Development - IC layout design</h2>

	<p>This position is with EDUSAFE partner University of Roma "Tor
		Vergata", Italy</p>
	<p>Experienced Researcher (ER) position for WRM chip development and IC
		layout design</p>
	<p>As an ER, you will participate in the development and formalization
		of a novel device, the "Weighting Resistive Matrix" (WRM), which will
		significantly enhance the present performance of pattern recognition
		systems. This is a high connectivity hybrid Analog-Digital circuit
		which projects an image from the real space to a given parametric
		space, with respect to an arbitrary hypothesis, with high reliability
		and noise rejection.</p>
	<p>You will develop the IC layout design in 12 &micro;m MOS technology
		and follow up the production process by the foundry on a multi-project
		run. You will be will be integrated in the team of the developers of
		this new device which includes physicists and engineers highly skilled
		in high performance A/D electronics. You will contribute directly to
		this challenging task.</p>
	<p>This task is part of a larger international collaboration coordinated by 
		the ATLAS experiment at the Large Hadron Collider, LHC, at CERN aiming 
		to develop a novel Augmented Reality system for operation in hazardous 
		areas. As such, you will also be part of the global system prototype 
		creation and testing campaigns in the LHC ATLAS experiment environment.</p>
	<p>Preferred university diploma: Master/Diploma degree in Electronic
		Engineering; PhD or equivalent experience in the IC design and
		development</p>
	<p>Requirements:</p>
	<ul>
		<li>Knowledge of IC design tools, specifically CADENCE</li>
		<li>Experience in full custom IC design</li>
		<li>Working experience and collaboration with foundries</li>
		<li>Good knowledge of English; basic knowledge of Italian or an
			undertaking to acquire it rapidly</li>
	</ul>
	<p>You will participate in network wide activities such as common test
		campaigns with Early Stage Researchers from other EDUSAFE partners,
		workshops, summer schools and conferences.</p>
	<p>You will spend up to 30% of your contract on secondments with
		EDUSAFE partners including Ecole Polytechnique Fédérale de Lausanne, EPFL, in Switzerland, 
		for computer vision, patch based recognition techniques, 3D reconstruction and pose
		determination, at PRISMA, in Greece and Democritus University of Thrace, DUTH, in Greece, 
		for system architecture design, microelectronics development, miniaturization and production process, 
		training on business and new product development process and at CERN, in Switzerland, 
		for project management, system integration and validation process methodology, 
		AR system environment and usability requirements for the LHC use case.</p>
	<p>This ER position will be under employment contract with University
		of Roma "Tor Vergata" as Research Associate. You will be based in
		Rome, Italy. The duration of the contract is 2 years expecting to start from about January 2014, full time.</p>

