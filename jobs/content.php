	<div class="row-fluid">
		<p>The EDUSAFE Marie Curie Initial Training Network offers a wide
			range of fellowships for research projects. Two types for fellowships
			are offered:</p>

		<dl>
			<dt>Early stage research fellowships (ESR):</dt>
			<dd>The fellowship has a duration of 36 months. ESRs are encouraged
				to participate in the PhD programme offered by the network partner.</dd>
			<dt>Experienced research fellowships (ER):</dt>
			<dd>The fellowship has a duration of 24 months and offers the
				possibility for scientific and complementary skill training
				(entrepreneurship, business and project management)</dd>
		</dl>

		<p>Fellowships are offered in the context of a Marie Curie Initial
			Training Network and transnational mobility is a key element of
			eligibility. As such, applications will only be accepted from
			candidates who have spent less than 12 months in country where the
			fellowship is hosted within the last three years. Candidates
			eligibility for the post is determined by Marie Curie terms and
			conditions.</p>

		<p>
			Fellowship covers monthly salary for two (ER) or three years (ESR)
			and other allowances in line with FP7- PEOPLE-2012-ITN. <a
				href="http://ec.europa.eu/research/mariecurieactions/index_en.htm">More
				information on Marie-Curie ...</a>
		</p>

		<p>
			For more information please contact the <a
				href="mailto:Olga.Beltramello@cern.ch">EDUSAFE Network Coordinator</a>.
		</p>
	</div>
