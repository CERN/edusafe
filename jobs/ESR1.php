	<h2>ESR 1 - WRM Chip Development - Mathematical properties and
		algorithms</h2>
	<p>This position is with EDUSAFE partner University of Roma "Tor
		Vergata", Italy</p>

	<p>Early Stage Researcher (ESR) position for the WRM chip development, 
	Mathematical properties and algorithms.</p>

	<p>As an ESR, the aim of your work is to participate in the development and formalization
		of a novel device the "Weighting Resistive Matrix" (WRM), aimed to enhance 
		by far the present performance of pattern recognition
		systems. This is a high connectivity hybrid Analog-Digital circuit
		which projects an image from the real space to a given parametric
		space, with respect to an arbitrary hypothesis, with high reliability
		and noise rejection.</p>
	<p>Your goal is to study the theoretical and mathematical aspects connected to
		the WRM, helping the design effort. These will range from the
		topological properties of the matrix nodes, to the study of
		implementation of the general Hough transform in the WRM system. Part
		of the work will consist of adapting to the WRM logic the algorithms
		developed by another EDUSAFE partner EPFL, Ecole Polytechnique
		F&eacute;d&eacute;rale de Lausanne, Switzerland, which is developing feature-point
		and image-patch based techniques which are more established and could
		be greatly accelerated by means of WRM.</p>
	<p>This task is part of a large collaboration coordinated by the ATLAS
		experiment at the Large Hadron Collider, LHC, at CERN aiming to develop 
		a novel Augmented Reality system for operation in hazardous areas. As such, 
		you will also be part of the global system prototype creation and
		testing campaigns in the LHC ATLAS experiment environment.</p>

	<p>Preferred university diploma: Master/Diploma degree in Mathematics /
		Applied Mathematics.</p>

	<p>Requirements:</p>
	<ul>
		<li>Knowledge in programming and languages (C, MatLab)</li>
		<li>Experience in mathematical methods of image analysis, topologic
			transforms</li>
		<li>Experience with Hough and Radon transform would be a plus</li>
		<li>Good knowledge of English; basic knowledge of Italian or an
			undertaking to acquire it rapidly</li>
	</ul>

	<p>You will spend up to 30% of your contract on secondments with
		EDUSAFE partners including at Ecole Polytechnique Fédérale de Lausanne,
		 EPFL, in Switzerland, for computer vision, patch based recognition techniques, 3D
		reconstruction and pose determination and at Novocaptis in Greece, and at the 
		Aristotle University of Thessaloniki, AUTH, in Greece, for system archetecture design,
		video data coding and transfer.</p>
	<p>You will participate in network wide activities such as common test
		campaigns with ESRs from the other EDUSAFE partners, workshops, summer
		schools and conferences.</p>
	<p>You will be offered the possibility to study for a PhD in
		Mathematics at University of Roma "Tor Vergata" using the research work
		performed within EDUSAFE project to obtain a PhD at the end of
		the 3-year contract.</p>
	<p>The ESR will be under employment contract with University
		of Roma "Tor Vergata". You will be based in Rome, Italy. The duration
		of the contract is 3 years expected to start from about January 2013, full time.</p>

