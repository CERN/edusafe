	<h2>Authoring and Visualizer Software - ESR4 - EDUSAFE</h2>
	<p>
		<strong>This position is with ITN partner: CERN, European Organization
			for Nuclear Research, Switzerland.</strong>
	</p>

	<p>Early Stage Researcher (ESR) position for the development of the ???
	</p>

	<p>As an ESR, the aim of your work is to participate to the
		architecture design and development of the authoring software creating
		the Virtual Reality and Augmented Reality data contents and the
		visualizer software that will display these data on various
		infrastructure displays. The software will be optimized to efficiently
		integrate the required Augmented Reality features in particular the
		parametric data from the 3D tracking system developed by other EDUSAFE
		partners, Novocaptis Greece and EPFL, Ecole Polytechnique Federale de
		Lausanne, Switzerland. You will analyse and develop further the
		Augmented Reality rendering methods as well as the transfer of large
		quantity of video data to the various display devices without
		degrading the system performances. You will work in close
		collaboration with TUM, Technical University of Munich, Germany, to
		develop the user interfaces scalable to various types of display
		devices.</p>
	<p>You will participate to the system prototype creation and testing
		campaigns in the LHC ATLAS experiment environment.</p>
	<p>You will spend a fraction of your time on secondments in
		collaborating institutions (EPFL, TUM) (on the following topics:
		computer vision, patch based recognition techniques, 3D reconstruction
		and pose determination, Augmented Reality applications and information
		visualization, rendering methods).</p>
	<p>Preferred university diploma: Master/Diploma degree in Software
		Engineering / Electronics.</p>
	<p>Requirements:</p>
	<ul>
		<li>Knowledge in programming and languages (C++, Java)</li>
		<li>Knowledge in web-based application development (HTML, GWT)</li>
		<li>Basic knowledge of electronics</li>
		<li>Knowledge of Augmented Reality technology and methods would be a
			plus</li>
		<li>Fluent English language is mandatory, French knowledge will be
			appreciated
			</li>
			</ul>
			
			<p>The candidate will participate in network wide activities
			such as common test campaigns with ESRs from the other EDUSAFE
			partners, workshops, summer schools and conferences.</p> <p>The appointed
			researcher will be offered the possibility to study for a PhD at NTUA
			National Technical University Athens, using the research work
			performed within the EDUSAFE project to obtain a PhD at the end of
			the 3 years appointment.</p> <p><strong>The ESR will be under employment contract
			with CERN, European Organization for Nuclear Research. The successful
			candidate will be based in Geneva, Switzerland. The duration of the
			contract is 3 years starting January 2013, full time and fix-term.</strong></p>
			
