	<h2>ESR 4 - Authoring and visualizer software</h2>
	<p>This position is with ITN partner: CERN, European Organization for
		Nuclear Research, Switzerland</p>
	<p>Early Stage Researcher (ESR) position for the development of the
		system authoring and visualizer software</p>
	<p>As an ESR, you will participate in the architecture design and
		development of the authoring software creating the Virtual Reality
		(VR) and Augmented Reality (AR) data contents and the visualizer
		software that will display these data on various infrastructure
		displays. The software will be optimized to efficiently integrate the
		required Augmented Reality features in particular the parametric data
		from the 3D tracking system developed by other EDUSAFE partners,
		Novocaptis Greece and EPFL, Ecole Polytechnique F&eacute;d&eacute;rale de Lausanne,
		Switzerland. You will analyse and further develop the AR rendering
		methods as well as the transfer of large quantity of video data to the
		various display devices without degrading the system performance. You
		will work in close collaboration with TUM, Technical University of
		Munich, Germany, to develop the user interfaces scalable to various
		types of display devices. You will participate in the system prototype
		creation and testing campaigns in the LHC ATLAS experiment
		environment.</p>
	<p>You will spend up to 30% of your time on secondments in
		collaborating institutions (EPFL, TUM) (on the following topics:
		computer vision, patch based recognition techniques, 3D reconstruction
		and pose determination, AR applications and information visualization,
		rendering methods)</p>
	<p>Preferred university diploma: Master/Diploma degree in Software
		Engineering / Electronics.</p>
	<p>Requirements:</p>
	<ul>
		<li>Knowledge in programming and languages (C++, Java,..)</li>
		<li>Knowledge in web-based application development (HTML, GWT)</li>
		<li>Basic knowledge of electronics</li>
		<li>Knowledge of AR technology and methods would be an advantage</li>
		<li>Good knowledge of English language; basic knowledge of Greek or an
			undertaking to acquire it rapidly</li>
	</ul>
	<p>You will participate in Network wide activities such as common test
		campaigns with ESRs from the other EDUSAFE partners, workshops, summer
		schools and conferences.</p>
	<p>You will be offered the possibility to study for a PhD at NTUA
		National Technical University Athens, Greece, using the research work
		performed within the EDUSAFE project to obtain a PhD at the end of the
		3-year appointment.</p>
	<p>This ESR position will be under employment contract with CERN,
		European Organization for Nuclear Research. You will be based in
		Geneva, Switzerland. The duration of the contract is 3 years starting
		January 2013, full time.</p>
