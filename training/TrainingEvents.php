	<h2>Training Events</h2>

<p>
EDUSAFE brings the researchers together several times a year by arranging training events, such as a kick-off week, joint training courses and summer schools. These events naturally serve also as great opportunities for networking. 
</p>

<p>
The kick-off week maps the course of the 4-year programme and introduces the researchers to the network. Summer Schools propose scientific courses, seminars and specialist lectures to ESRs and ERs. Joint Training Events bring together fellows to exchange their researcher experience and serves to strengthen the sense of community within and to build strong future networks for the fellows. Final Conference will showcase the EDUSAFE results to the wider community, industry and public.
</p>

